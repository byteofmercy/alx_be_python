# Let's find out how old you will be in the year 2050!

# Ask the user their age
age_now = input("How old are you? ")

# Turn that answer into a number
age_now = int(age_now)

# Add 27 years to their age (because 2050 is 27 years from 2023)
age_2050 = age_now + 27

# Tell them the result
print("In 2050, you will be", age_2050, "years old.")
